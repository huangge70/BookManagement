package com.domain;

public class Retrieval {

	private int id;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getRetrieval() {
		return retrieval;
	}
	public void setRetrieval(String retrieval) {
		this.retrieval = retrieval;
	}
	public int getTime() {
		return time;
	}
	public void setTime(int time) {
		this.time = time;
	}
	private String retrieval;
	private int time;
}
